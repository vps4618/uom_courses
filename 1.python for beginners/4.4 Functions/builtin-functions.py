# finding maximum number between two numbers
a = 100
b = 120
ab = str(max(a, b))
print("Maximum number is " + ab + " between " + str(a) + " and " + str(b))

# finding minimum number between two numbers
c = 154
d = 187
e = str(min(c, d))
print("Minimum number is " + e + " between " + str(c) + " and " + str(d))
